  <h2><u>Oliver Scott Ministries</u></h2>
  <p><b>The vision of Oliver Scott Ministries is to authentically express the truth through various giftings (primarily music and the spoken word) with the goal of relevance, clarity, integrity and change.</b></p>
								<p>We envision ministry flowing from the place of intimate relationship with God, self and others.</p>
								<p>We envision our ministry being part of the FREEDOM MOVEMENT that contributes to a better quality of life for all peoples while advocating the liberation of all oppressed peoples who live in the struggle every day. God has given us The Truth in the person of His Son Jesus Christ. The Son is The Song and if a song is to express The Truth it must speak of Him. When we live by and proclaim The Truth, the Vision of freedom becomes a tangible reality.</p>
								<p>To speak of Him lyrically, we speak what His word says directly from scripture or we speak of personal experiences where scripture has informed us within the context of our personal experiences. We speak and offer (by faith); He judges and rewards accordingly...</p>
								<p>To speak of Him musically is more subjective and only He can judge as to whether the music speaks authentically of Him. Again, we speak and offer (by faith); He judges and rewards accordingly...</p>
  
